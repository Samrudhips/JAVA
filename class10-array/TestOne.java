//Arrays in Java
public class TestOne {
    public static void main(String[] args) {
        float[] eids;//declaration
        eids =new float[4];//Creation
        eids[0]=101;//Initilization
        eids[1]=102f;//stores based on indexing
        eids[2]=103f;
        System.out.println(eids[0]);//reading an array 101
        System.out.println(eids[1]);//102
        System.out.println(eids[2]);//103
        System.out.println(eids[3]);//0 
        System.out.println(eids);//getting hexadecimal address,address of object
    }
   

    
}
